package day5.discussion;

public class ArithmeticOperator {
    static void main(String[] args) {
        //+ - * / %

        int x = 5;
        int y = 2;

        int sum = x + y;

        System.out.println("Sum: " + sum);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
