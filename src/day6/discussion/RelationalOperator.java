package day6.discussion;

public class RelationalOperator {
    static void main(String[] args) {
        // < > <= >= == != returns boolean
        int x = 1;
        int y = 2;

        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x < y);
        System.out.println(x > y);
    }
}
