package day4.activity;

public class Activity1_Villamor {
    public static void main(String[] args) {
        // Declare and initialize variables of each data type
        boolean isStudent = true;
        byte age = 27;
        short year = 2026;
        int population = 1_000_000;
        long distance = 9_460_000_000_000L;
        float temperature = 36.6f;
        double price = 199.99;
        char grade = 'A';
        String name = "Josiah Villamor";

        // Print out the values with labels
        System.out.println("boolean isStudent: " + isStudent);
        System.out.println("byte age: " + age);
        System.out.println("short year: " + year);
        System.out.println("int population: " + population);
        System.out.println("long distance: " + distance);
        System.out.println("float temperature: " + temperature);
        System.out.println("double price: " + price);
        System.out.println("char grade: " + grade);
        System.out.println("String name: " + name);
    }
}
